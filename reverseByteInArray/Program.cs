        static byte revrseByte(byte number)
        {
            byte reversed = 0;
            int i;
            if (number != 0)
            {
                for (i = 0; i < 8; i++)
                {
                    if ((number & (1 << i)) != 0)
                        reversed |= (byte)(1 << (7 - i));
                }
                //reversed |= (byte)((byteToReverse & 0x01) << 7);
                //reversed |= (byte)((byteToReverse & 0x02) << 5);
                //reversed |= (byte)((byteToReverse & 0x04) << 3);
                //reversed |= (byte)((byteToReverse & 0x08) << 1);
                //reversed |= (byte)((byteToReverse & 0x10) >> 1);
                //reversed |= (byte)((byteToReverse & 0x20) >> 3);
                //reversed |= (byte)((byteToReverse & 0x40) >> 5);
                //reversed |= (byte)((byteToReverse & 0x80) >> 7);        
            }
            return reversed;
        }

        static void Main(string[] args)
        {
            string text;
            string sPattern = "0x[0-9a-fA-F]{2}";
            if (args.Length != 1)
            {
                Console.WriteLine("reverseByteInArray version 1.00");
                Console.WriteLine("Usage : Convert a file with hex string to reversed in byte hex string");
                Console.WriteLine("Format: reverseByteInArray filepath");
                return;
            }
            var fileStream = new FileStream(args[0], FileMode.Open, FileAccess.Read);
            using (StreamWriter fileConverted = new StreamWriter("spectra-" + args[0] , false))
            {
                fileConverted.WriteLine("const unsigned char spectra_logo [] = {");
                using (var streamReader = new StreamReader(fileStream, Encoding.UTF8))
                {
                    int i = 1;
                    text = streamReader.ReadToEnd();
                    var matches = Regex.Matches(text, sPattern);
                    foreach (var match in matches)
                    {

                        byte number = (byte)Convert.ToInt32(match.ToString().Remove(0, 2), 16);
                        number = revrseByte(number);
                        string str = string.Format("0x{0:X2}, ", number);
                        fileConverted.Write(str);
                        Console.Write(str);
                        if (i % 16 == 0)
                        {
                            fileConverted.Write("\r\n");
                            Console.WriteLine("");
                        }
                        i++;
                    }
                    fileConverted.WriteLine("};");
                }
